package com.thuchanh;

public class Variable {
    public static void main(String[] args) {
        int i = 10;
        System.out.println(i);
        float f = 20.5f;
        System.out.println(f);
        double d = 20.5d;
        System.out.println(d);
        boolean b = true;
        System.out.println(b);
        char c = 'a';
        System.out.println(c);
        String s = "Hà Nội";
        System.out.println(s);
    }
}
